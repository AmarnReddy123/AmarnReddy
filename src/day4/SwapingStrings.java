package day4;

public class SwapingStrings {

	public static void main(String[] args) {

		String s1="abc";
		String s2="xyz";
		
		String s;
		
        System.out.println("Before swaping.....");
		
		System.out.println("s1 is:"+s1);
		System.out.println("s2 is:"+s1);
		
		s=s1;
		s1=s2;
		s2=s;
		
		System.out.println("After swaping.....");
		
		System.out.println("s1 is:"+s1);
		System.out.println("s2 is:"+s1);


		
		
		
		
	}

}
